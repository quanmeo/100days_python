import random as rd

word = rd.choice(word_list).lower()

print(f'You have total {len(stages)} times guess wrong')
print(f'Please guess a word has {len(word)} characters')


found_words = []

def print_word():
    for i in word:
        if not i in found_words:
            print('_', end='')
        else:
            print(i, end='')
        print(' ', end='')
    print()


words = set()
max_words = set(word)

count = 0
while True:
    print()
    print_word()
    print()
    character = input('Character: ')
    if character in words:
        print('Please try another character that you did guess before')
        continue
    words.add(character)
    if character in word:
        print('Congrats, that character found')
        found_words.append(character)
        if len(found_words) == len(max_words):
            print('Comgrats, you are guess full characters of the word')
            break
    else:
        count += 1
        print(stages[0 - count])
        if count == len(stages):
            print('Game over')
            break
        else:
            print('Wrong character, please try again')
